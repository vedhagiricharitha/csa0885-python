def check(li,element):
    count=0
    for i in li:
         if i==element:
             count=count+1  
    return count

li=[]
len=int(input("Range: "))
for i in range (0,len):
    ele=int(input("Enter Elements: "))
    li.append(ele)


element=int(input("Enter Element to find: "))
print("Element",element,"present in",(li))
print(check(li,element))
